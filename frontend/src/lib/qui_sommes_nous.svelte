<div class="presentation_page">

    <p>  <strong>O’refuge </strong>est une Association loi 1901 ayant pour mission le sauvetage et l’adoption d’animaux de chiens et de chats.<br>

        Fondé en 2023, notre refuge est basé en Ille et Vilaine, en Bretagne, et comprend 4 bénévoles et de nombreuses familles d’accueil.
        Nous avons décidé, en créant ce refuge pour chiens et chats, de consacrer notre énergie à la protection des chiens et chats.<br>
        
        
        Recueillir les chiens et chats abandonnés et/ou maltraités.
        Faire adopter et donner une seconde chance aux animaux.
        Soigner les animaux grâce à la participation bénévole de vétérinaire.
        Sensibiliser l’ensemble de la population aux droits et devoirs envers les animaux.
        Lutter contre toute forme de maltraitance, y compris l’abandon et le trafic.
        Éduquer les générations de demain avec pédagogie au respect de l’animal.
        Sensibilisation de la population à l’importance de la stérilisation des chiens et chats.<br>
        
        
        Depuis la création de O’refuge, notre équipe de bénévoles a permis l’adoption de nombreux animaux. Malheureusement, les abandons et divagations de chiens et chats errants sont bien plus nombreux et nous faisons souvent face à un manque de place au sein de notre refuge.<br>
        
        Uniquement financé par des dons de particuliers, O’refuge est constamment en recherche de :<br>
        <ul>

     <li>   Don financier</li>
     <li>   Don alimentaire (croquettes pour chats et chiens de tout type)</li>
     <li>   Don de couverture pour les animaux du refuge</li>
     <li>   Don de jouets pour chat et chiens</li>
     <li>   Don d’accessoires (laisse, collier, harnais, gamelle)</li>
     <li>   Mais aussi don d’équipement pour le refuge.</li><br>
        
